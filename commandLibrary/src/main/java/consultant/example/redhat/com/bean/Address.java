package consultant.example.redhat.com.bean;

public class Address {
	private String building;
	private String districtCode;
	private String fullEnglishAddress;
	private String record_id;
	public String getBuilding() {
		return building;
	}
	public void setBuilding(String building) {
		this.building = building;
	}
	public String getDistrictCode() {
		return districtCode;
	}
	public void setDistrictCode(String districtCode) {
		this.districtCode = districtCode;
	}
	public String getFullEnglishAddress() {
		return fullEnglishAddress;
	}
	public void setFullEnglishAddress(String fullEnglishAddress) {
		this.fullEnglishAddress = fullEnglishAddress;
	}
	public String getRecord_id() {
		return record_id;
	}
	public void setRecord_id(String record_id) {
		this.record_id = record_id;
	}
	
}
