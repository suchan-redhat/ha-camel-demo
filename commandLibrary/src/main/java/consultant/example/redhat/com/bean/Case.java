package consultant.example.redhat.com.bean;

public class Case {
	private String admissionDatetime;
	private String hospitalCode;
	private String number;
	private String patientType;
	private String sourceCode;
	private String sourceIndicator;
	private String specialty;
	private String type;
	private String wardClass;
	private String wardCode;
	public String getAdmissionDatetime() {
		return admissionDatetime;
	}
	public void setAdmissionDatetime(String admissionDatetime) {
		this.admissionDatetime = admissionDatetime;
	}
	public String getHospitalCode() {
		return hospitalCode;
	}
	public void setHospitalCode(String hospitalCode) {
		this.hospitalCode = hospitalCode;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getPatientType() {
		return patientType;
	}
	public void setPatientType(String patientType) {
		this.patientType = patientType;
	}
	public String getSourceCode() {
		return sourceCode;
	}
	public void setSourceCode(String sourceCode) {
		this.sourceCode = sourceCode;
	}
	public String getSourceIndicator() {
		return sourceIndicator;
	}
	public void setSourceIndicator(String sourceIndicator) {
		this.sourceIndicator = sourceIndicator;
	}
	public String getSpecialty() {
		return specialty;
	}
	public void setSpecialty(String specialty) {
		this.specialty = specialty;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getWardClass() {
		return wardClass;
	}
	public void setWardClass(String wardClass) {
		this.wardClass = wardClass;
	}
	public String getWardCode() {
		return wardCode;
	}
	public void setWardCode(String wardCode) {
		this.wardCode = wardCode;
	}

}
